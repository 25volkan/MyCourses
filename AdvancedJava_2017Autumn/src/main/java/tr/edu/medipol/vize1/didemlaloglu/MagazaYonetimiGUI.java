    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        try{
        StokBase stok = null;
        
        int stok_adedi = Integer.parseInt(jTextField4.getText());
        String urun = jComboBox3.getSelectedItem().toString();
        
        switch(urun){
            case "Çanta": stok = new Canta(stok_adedi, urun);break;
            case "Ayakkabı": stok = new Ayakkabi( stok_adedi, urun); break;
            case "Etek": stok = new Etek(stok_adedi, urun); break; 
            case "Patates": stok = new Patates(stok_adedi, urun); break; 
        }
        String item = stok.getUrun()+ " " +stok.getStok_sayisi() + " Adet";
        ((DefaultListModel) jList2.getModel()).addElement(item);
        }
        catch(Exception e){
            
        }
    }//GEN-LAST:event_jButton4ActionPerformed

