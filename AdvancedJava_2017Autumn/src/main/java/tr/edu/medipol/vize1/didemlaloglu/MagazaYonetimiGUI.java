    private void SaveToFile(DefaultListModel model, String dosyaAdi) throws FileNotFoundException, UnsupportedEncodingException{
        PrintWriter writer = new PrintWriter(dosyaAdi, "UTF-8");
        try {
                int len = model.getSize();
                for (int i = 0; i < len; i++) {
                    writer.println(model.getElementAt(i).toString());
                }
            } finally {
                writer.close();
            }
    }
    private void ReadFromFile() throws FileNotFoundException, IOException{
        BufferedReader br = new BufferedReader(new FileReader("calisanlar.txt"));
try {
    StringBuilder sb = new StringBuilder();
    String line = br.readLine();

    while (line != null) {
        ((DefaultListModel) jList1.getModel()).addElement(line);
        line = br.readLine();
    }
} finally {
    br.close();
}
        br = new BufferedReader(new FileReader("stoklar.txt"));
try {
    StringBuilder sb = new StringBuilder();
    String line = br.readLine();

    while (line != null) {
        ((DefaultListModel) jList2.getModel()).addElement(line);
        line = br.readLine();
    }
} finally {
    br.close();
}
    }
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
        CalisanBase calisan = null;
        
        String ad = jTextField1.getText();
        String soyad = jTextField2.getText();
        String meslek = jComboBox1.getSelectedItem().toString();
        
        switch(meslek){
            case "Stajyer": calisan = new Stajyer(ad, soyad);break;
            case "Personel": calisan = new Personel(ad, soyad); break;
            case "Mudur": calisan = new Mudur(ad,soyad); break; 
        }
        String item = calisan.getMeslek() + " " + calisan.getAd() + " " + calisan.getSoyad();
        ((DefaultListModel) jList1.getModel()).addElement(item);
        }
        catch(Exception e){
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

