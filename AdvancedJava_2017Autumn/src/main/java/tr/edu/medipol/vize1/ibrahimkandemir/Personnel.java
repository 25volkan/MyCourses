package tr.edu.medipol.vize1.ibrahimkandemir;


public class Personnel {
	protected String employeeNameSurname, employeeTCNo, registrationNo, employeeJobDate, employeeSalary, employeeDepartment;
		
	public Personnel(String employeeNameSurname, String employeeTCNo, String registrationNo, 
			String employeeJobDate, String employeeSalary, String employeeDepartment) {
	
		this.employeeNameSurname = employeeNameSurname;
		this.employeeTCNo = employeeTCNo;
		this.registrationNo = registrationNo;
		this.employeeJobDate = employeeJobDate;
		this.employeeSalary = employeeSalary;
		this.employeeDepartment = employeeDepartment;
	}

	
}
